#pragma once

#include <xrn/Engine/Component/Detail/Vector3.hpp>

namespace xrn::engine::component { class Control; }

namespace xrn::engine::component {

///////////////////////////////////////////////////////////////////////////
/// \brief Direction in degree
/// \ingroup vulkan
///
/// \include Direction.hpp <xrn/Engine/Component/Direction.hpp>
///
///////////////////////////////////////////////////////////////////////////
class Direction
    : public ::xrn::engine::component::detail::Vector3<Direction>
{

public:

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Static
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    EXPOSE_VECTOR3_METHODS(Direction);

    static inline const ::glm::vec3 defaultDirection{
        ::glm::normalize(::glm::vec3(
            ::glm::cos(::glm::radians(0.0f)) * ::glm::cos(::glm::radians(0.0f))
            , ::glm::sin(::glm::radians(0.0f))
            , ::glm::sin(::glm::radians(0.0f)) * ::glm::cos(::glm::radians(0.0f))
        ))
    };




public:

    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Constructor
    //
    ///////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////////////////////////

    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    explicit Direction();

    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    explicit Direction(
        float value
    );

    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    explicit Direction(
        ::glm::vec3 offset
    );

    ///////////////////////////////////////////////////////////////////////////
    ///
    ///////////////////////////////////////////////////////////////////////////
    explicit Direction(
        float valueX
        , float valueY
        , float valueZ
    );

};

} // namespace xrn::engine::component
