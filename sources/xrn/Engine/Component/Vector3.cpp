///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Vector3::Vector3()
    : ::xrn::engine::component::detail::Vector3<Vector3, false>{ ::glm::vec3{ 0.f, 0.f, 0.f } }
{}

///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Vector3::Vector3(
    const float value
)
    : ::xrn::engine::component::detail::Vector3<Vector3, false>{ ::glm::vec3{ value, value, value } }
{}

///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Vector3::Vector3(
    const ::glm::vec3& value
)
    : ::xrn::engine::component::detail::Vector3<Vector3, false>{ ::std::move(value) }
{}

///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Vector3::Vector3(
    const float valueX
    , const float valueY
    , const float valueZ
)
    : ::xrn::engine::component::detail::Vector3<Vector3, false>{
        ::glm::vec3{ valueX, valueY, valueZ }
    }
{}
