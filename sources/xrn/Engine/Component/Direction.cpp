///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Direction::Direction()
    : ::xrn::engine::component::detail::Vector3<Direction>{ ::glm::vec3{ 0.f, 0.f, 0.f } }
{}

///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Direction::Direction(
    const float value
)
    : ::xrn::engine::component::detail::Vector3<Direction>{ ::glm::vec3{ value, value, value } }
{}

///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Direction::Direction(
    ::glm::vec3 value
)
    : ::xrn::engine::component::detail::Vector3<Direction>{ ::std::move(value) }
{}

///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Direction::Direction(
    const float valueX
    , const float valueY
    , const float valueZ
)
    : ::xrn::engine::component::detail::Vector3<Direction>{
        ::glm::vec3{ valueX, valueY, valueZ }
    }
{}
