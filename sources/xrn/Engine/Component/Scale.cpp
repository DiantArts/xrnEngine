#include <pch.hpp>
#include <xrn/Engine/Component/Scale.hpp>



///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////
// Constructors
//
///////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Scale::Scale()
    : ::xrn::engine::component::detail::Vector3<Scale>{ ::glm::vec3{ 0.f, 0.f, 0.f } }
{}

///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Scale::Scale(
    const float value
)
    : ::xrn::engine::component::detail::Vector3<Scale>{ ::glm::vec3{ value, value, value } }
{}

///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Scale::Scale(
    const ::glm::vec3& value
)
    : ::xrn::engine::component::detail::Vector3<Scale>{ ::std::move(value) }
{}

///////////////////////////////////////////////////////////////////////////
///
///////////////////////////////////////////////////////////////////////////
::xrn::engine::component::Scale::Scale(
    const float valueX
    , const float valueY
    , const float valueZ
)
    : ::xrn::engine::component::detail::Vector3<Scale>{
        ::glm::vec3{ valueX, valueY, valueZ }
    }
{}
